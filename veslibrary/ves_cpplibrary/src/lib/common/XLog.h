#pragma once

#include <string>

namespace vagt
{
    namespace log
    {
        /*************************************************************************************************//**
        * @brief Log level 
        *****************************************************************************************************/
        enum XLogLevel
        {
            XLogTrace,
            XLogDebug,
            XLogInfo,
            XLogWarn,
            XLogErr,
            XLogCritical,
            XLogOff,
        };

        /*************************************************************************************************//**
        *    Initialize the file logging. 
        *
        *    @note           To disable logging, set the path to "/dev/null" .
        *****************************************************************************************************/
        void init(const std::string& path, int logFileSize = 1048576 * 5, int logFileNum = 10);

        /*************************************************************************************************//**
        *    Set log level.
        *****************************************************************************************************/
        void setLevel(XLogLevel level);
    }
}
